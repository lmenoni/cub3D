#include "libft.h"

void	*ft_memset(void *str, int c, size_t len)
{
	size_t		i;
	char		*s;

	s = (char *)str;
	i = 0;
	while (i < len)
	{
		s[i] = c;
		i++;
	}
	return (str);
}
