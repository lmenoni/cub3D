#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*r;
	unsigned int	i;

	r = (char *)malloc((ft_strlen(s) + 1) * sizeof(char));
	if (!r || !s || !f)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		r[i] = f(i, s[i]);
		i++;
	}
	r[i] = '\0';
	return (r);
}
