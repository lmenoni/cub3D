#include "libft.h"

int	ft_atoi(const char *s)
{
	int		sign;
	int		r;
	size_t	i;

	sign = 1;
	r = 0;
	i = 0;
	while (s[i] == ' ' || (s[i] >= 9 && s[i] <= 13))
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			sign = -sign;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		r = (r * 10) + (s[i] - '0');
		i++;
	}
	r = r * sign;
	return (r);
}
